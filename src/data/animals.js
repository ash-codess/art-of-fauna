// Each image is a verified public-domain natural-history plate sourced from
// Wikimedia Commons (see scripts/fetch-images.ps1 + src/data/images.json).
// `file` is the Commons filename; we resolve it to a scaled thumbnail at
// runtime via Special:FilePath (see imageUrl below) so we never ship the
// 11,000px originals to the browser.

const COMMONS = 'https://commons.wikimedia.org/wiki/Special:FilePath/'

/**
 * Build a scaled, hot-linkable URL for a Commons file.
 * Special:FilePath redirects to the current file and honours ?width= to
 * return a server-rendered thumbnail — ideal for these enormous plates.
 */
export function imageUrl(file, width = 1100) {
  return `${COMMONS}${encodeURIComponent(file)}?width=${width}`
}

